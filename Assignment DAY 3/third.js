console.log("Using Switch case to check grade ");

//if else if else
let n =prompt("Enter your mark:");
if(n>=90 && n<=100){
    console.log(`Marks are ${n} and grade is O`);
}
else if (n>=80 && n<90){
    console.log(`Marks are ${n} and grade is A`);
}
else if (n>=70 && n<80){
    console.log(`Marks are ${n} and grade is B`);
}
else if (n>=60 && n<70){
    console.log(`Marks are ${n} and grade is C`);
}
else if (n>=50 && n<60){
    console.log(`Marks are ${n} and grade is D`);
}

else{
    console.log(`Marks are ${n} and grade is E`);
}









